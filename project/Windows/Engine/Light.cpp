#include "Light.h"

//*****************************************************************************
// Constructor
//*****************************************************************************
Light::Light()
{
    m_nType   = LIGHT_NO_TYPE;
    m_nEnable = 1;

    m_arColor[0] = 1.0f;
    m_arColor[1] = 1.0f;
    m_arColor[2] = 1.0f;
    m_arColor[3] = 1.0f;
}

//*****************************************************************************
// Destructor
//*****************************************************************************
Light::~Light()
{

}

//*****************************************************************************
// SetEnable
//*****************************************************************************
void Light::SetEnable(int nEnable)
{
    m_nEnable = nEnable;
}

//*****************************************************************************
// IsEnabled
//*****************************************************************************
int Light::IsEnabled()
{
    return m_nEnable;
}

//*****************************************************************************
// SetColor
//*****************************************************************************
void Light::SetColor(float fRed,
                     float fGreen,
                     float fBlue,
                     float fAlpha)
{
    m_arColor[0] = fRed;
    m_arColor[1] = fGreen;
    m_arColor[2] = fBlue;
    m_arColor[3] = fAlpha;
}

//*****************************************************************************
// GetType
//*****************************************************************************
int Light::GetType()
{
    return m_nType;
}
