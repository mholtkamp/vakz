#include "Effect.h"

//*****************************************************************************
// Constructor
//*****************************************************************************
Effect::Effect()
{
    m_nType    = EFFECT_NO_TYPE;
    m_nEnabled = 1;
}

//*****************************************************************************
// Destructor
//*****************************************************************************
Effect::~Effect()
{

}

//*****************************************************************************
// IsEnabled
//*****************************************************************************
int Effect::IsEnabled()
{
    return m_nEnabled;
}

//*****************************************************************************
// SetEnable
//*****************************************************************************
void Effect::SetEnable(int nEnable)
{
    m_nEnabled = nEnable;
}

//*****************************************************************************
// GetType
//*****************************************************************************
int Effect::GetType()
{
    return m_nType;
}
